"""Benchmark every criterion across data types, sample sizes, and effects.

Outputs a markdown table to ``docs/benchmark.md`` and accompanying PNGs to
``docs/img/``. Re-run with ``uv run python scripts/benchmark.py``.

Wall-clock: ~2 min on a modern laptop with default settings.
"""

from __future__ import annotations

import argparse
import time
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import pandas as pd

from absim import EffectSize, Simulator
from absim.criteria import (
    CUPED,
    Bootstrap,
    DeltaMethod,
    Linearization,
    PairedStratification,
    PostStratification,
    WelchTTest,
    ZTestProportions,
)
from absim.generators import BinaryGenerator, ContinuousGenerator, RatioGenerator
from absim.reports import plot_fpr_bar

ROOT = Path(__file__).resolve().parent.parent


def build_scenarios(n_sims: int):
    """Return a list of (label, generator, criteria, effects) tuples."""
    cont_effects = [
        EffectSize(name="none", value=0.0),
        EffectSize(name="small", value=0.05),
        EffectSize(name="medium", value=0.10),
        EffectSize(name="large", value=0.20),
    ]
    bin_effects = [
        EffectSize(name="none", value=0.0),
        EffectSize(name="small", value=0.005),
        EffectSize(name="medium", value=0.010),
        EffectSize(name="large", value=0.020),
    ]
    ratio_effects = [
        EffectSize(name="none", value=0.0, relative=True),
        EffectSize(name="small", value=0.02, relative=True),
        EffectSize(name="medium", value=0.05, relative=True),
        EffectSize(name="large", value=0.10, relative=True),
    ]

    scenarios = []
    for n in (1_000, 5_000, 20_000):
        scenarios.append(
            (
                f"continuous_n{n}",
                ContinuousGenerator(n_per_group=n, mean=0.0, sd=1.0, rho=0.6),
                {
                    "welch": WelchTTest(),
                    "cuped": CUPED(),
                    "post_strat": PostStratification(),
                    "bootstrap": Bootstrap(n_resamples=400, method="percentile"),
                },
                cont_effects,
            )
        )
        scenarios.append(
            (
                f"paired_n{n}",
                ContinuousGenerator(n_per_group=n, rho=0.6, paired=True),
                {
                    "welch": WelchTTest(),
                    "paired": PairedStratification(paired=True),
                    "cuped": CUPED(),
                },
                cont_effects,
            )
        )
        scenarios.append(
            (
                f"binary_n{n}",
                BinaryGenerator(n_per_group=n, p=0.1, rho=0.0),
                {
                    "z_proportion": ZTestProportions(),
                    "welch": WelchTTest(),
                },
                bin_effects,
            )
        )
        scenarios.append(
            (
                f"ratio_n{n}",
                RatioGenerator(n_per_group=n, base_rate=0.2, sessions_mean=5.0),
                {
                    "delta": DeltaMethod(),
                    "linearization": Linearization(),
                    "welch_naive": WelchTTest(),
                },
                ratio_effects,
            )
        )
    return scenarios, n_sims


def run(scenarios, n_sims: int) -> pd.DataFrame:
    rows = []
    for label, gen, criteria, effects in scenarios:
        for crit_label, crit in criteria.items():
            for eff in effects:
                t0 = time.perf_counter()
                sim = Simulator(
                    generator=gen,
                    criterion=crit,
                    n_sims=n_sims,
                    alpha=0.05,
                    effect=eff,
                    seed=0,
                )
                rep = sim.run()
                dt = time.perf_counter() - t0
                rows.append(
                    {
                        "scenario": label,
                        "criterion": crit_label,
                        "n_per_group": getattr(gen, "n_per_group", None),
                        "effect": eff.name,
                        "effect_value": eff.value,
                        "rejection_rate": rep.rejection_rate,
                        "ci_low": rep.binomial_ci_low,
                        "ci_high": rep.binomial_ci_high,
                        "runtime_sec": dt,
                    }
                )
                print(
                    f"{label:18s} {crit_label:14s} {eff.name:6s}  "
                    f"rate={rep.rejection_rate:.4f}  ({dt:.2f}s)"
                )
    return pd.DataFrame(rows)


def write_outputs(df: pd.DataFrame, out_md: Path, img_dir: Path) -> None:
    img_dir.mkdir(parents=True, exist_ok=True)
    md = ["# Benchmark", "", "Auto-generated by `scripts/benchmark.py`.", ""]

    # Markdown table grouped by scenario (hand-rolled to avoid `tabulate`).
    for scenario, sub in df.groupby("scenario"):
        md.append(f"## {scenario}")
        md.append("")
        pivot = sub.pivot(index="criterion", columns="effect", values="rejection_rate")
        cols = [c for c in ("none", "small", "medium", "large") if c in pivot.columns]
        pivot = pivot[cols]
        header = "| criterion | " + " | ".join(cols) + " |"
        sep = "|" + "|".join(["---"] * (len(cols) + 1)) + "|"
        md.append(header)
        md.append(sep)
        for crit_name, row in pivot.iterrows():
            cells = " | ".join(f"{row[c]:.4f}" if pd.notna(row[c]) else "—" for c in cols)
            md.append(f"| {crit_name} | {cells} |")
        md.append("")

        # Power curve PNG for this scenario.
        from absim.reports import plot_power_curve as _ppc

        # Build report-shaped objects on the fly for plotting.
        from absim.types import EffectSize, SimulationReport

        reps = [
            SimulationReport(
                criterion_name=r["criterion"],
                n_sims=int(df["rejection_rate"].size),  # placeholder
                alpha=0.05,
                effect=EffectSize(name=r["effect"], value=r["effect_value"], relative=False),
                rejection_rate=float(r["rejection_rate"]),
                binomial_ci_low=float(r["ci_low"]),
                binomial_ci_high=float(r["ci_high"]),
                mean_pvalue=float("nan"),
                mean_effect=float("nan"),
                mean_std_error=float("nan"),
                runtime_sec=float(r["runtime_sec"]),
            )
            for _, r in sub.iterrows()
        ]
        fig = _ppc(reps, title=f"Power vs effect — {scenario}")
        path = img_dir / f"power_{scenario}.png"
        fig.savefig(path, dpi=110)
        plt.close(fig)
        md.append(f"![{scenario}](img/{path.name})")
        md.append("")

        # FPR bar (only h0).
        h0 = [r for r in reps if r.effect.value == 0.0]
        if h0:
            try:
                fig = plot_fpr_bar(h0)
                fpr_path = img_dir / f"fpr_{scenario}.png"
                fig.savefig(fpr_path, dpi=110)
                plt.close(fig)
                md.append(f"![{scenario} FPR](img/{fpr_path.name})")
                md.append("")
            except ValueError:
                pass

    out_md.write_text("\n".join(md))
    print(f"wrote {out_md}")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--n-sims",
        type=int,
        default=2000,
        help="number of Monte Carlo iterations per (scenario, criterion, effect)",
    )
    args = parser.parse_args()
    scenarios, n_sims = build_scenarios(args.n_sims)
    df = run(scenarios, n_sims)
    df.to_csv(ROOT / "docs" / "benchmark.csv", index=False)
    write_outputs(df, ROOT / "docs" / "benchmark.md", ROOT / "docs" / "img")
    print("done.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
